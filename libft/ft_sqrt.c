#include "libft.h"

int		ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 2;
	if (nb == 1)
		return (1);
	if (nb >= 4 && nb <= 2147483647)
	{
		while (sqrt <= 46340)
		{
			if (sqrt * sqrt == nb)
				return (sqrt);
			sqrt++;
		}
	}
	return (0);
}
