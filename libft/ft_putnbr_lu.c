#include "libft.h"

void			ft_putnbr_lu(unsigned long n)
{
	if (n < 10)
		ft_putchar(n + '0');
	if (n >= 10)
	{
		ft_putnbr_lu(n / 10);
		ft_putnbr_lu(n % 10);
	}
}
